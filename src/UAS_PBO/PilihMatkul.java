    private void btnTambahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTambahActionPerformed
         if (kelas != null && !kelas.isEmpty()) {
            try {
                int sksInt = Integer.parseInt(sks);
                int semesterInt = Integer.parseInt(semester);
                String id_user = User.getInstance().getUsername();

                try (Connection conn = koneksi.getConnection();
                     PreparedStatement stmt = conn.prepareStatement("SELECT SUM(sks) FROM tbl_krs WHERE id_akun = ?");
                     PreparedStatement stmt2 = conn.prepareStatement("SELECT * FROM tbl_krs WHERE nama = ? AND id_akun = ?");
                     PreparedStatement stmt3 = conn.prepareStatement("INSERT INTO tbl_krs (id_akun, kelas, kode, nama, wp, semester, sks) VALUES (?, ?, ?, ?, ?, ?, ?)")) {

                    stmt.setString(1, id_user);

                    ResultSet rs = stmt.executeQuery();
                    if (rs.next()) {
                        int totalSks = rs.getInt(1);
                        if (totalSks + sksInt <= 24) {
                            stmt2.setString(1, nama);
                            stmt2.setString(2, id_user);

                            ResultSet rs2 = stmt2.executeQuery();

                            if (rs2.next()) {
                                JOptionPane.showMessageDialog(this, "Nama Mata Kuliah Sudah Ada");
                            } else {
                                stmt3.setString(1, id_user);
                                stmt3.setString(2, kelas);
                                stmt3.setString(3, kode);
                                stmt3.setString(4, nama);
                                stmt3.setString(5, wp);
                                stmt3.setInt(6, semesterInt);
                                stmt3.setInt(7, sksInt);

                                int rowsAffected = stmt3.executeUpdate();

                                if (rowsAffected > 0) {
                                    JOptionPane.showMessageDialog(this, "Penambahan Berhasil!");
                                    menuUser user = new menuUser();
                                    user.tampilData();
                                    user.hitungSKS(); // Update SKS count
                                    user.setVisible(true);
                                    this.dispose();
                                } else {
                                    JOptionPane.showMessageDialog(this, "Gagal Menambahkan Data");
                                }
                            }
                        } else {
                            JOptionPane.showMessageDialog(this, "SKS Melebihi Batas Maksimal (24)");
                        }
                    }
                } catch (SQLException ex) {
                    Logger.getLogger(PilihMatkul.class.getName()).log(Level.SEVERE, null, ex);
                }
            } catch (NumberFormatException e) {
                JOptionPane.showMessageDialog(this, "Semester dan SKS harus berupa angka.");
            }
        } else {
            JOptionPane.showMessageDialog(this, "Belum Ada Mata Kuliah Yang Dipilih");
        }
    }//GEN-LAST:event_btnTambahActionPerformed

