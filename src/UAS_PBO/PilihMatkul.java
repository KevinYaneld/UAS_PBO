    public void tampilData() {
        int row = tblKRS.getRowCount();
        for (int i = 0; i < row; i++) {
            model.removeRow(0);
        }
        String kategori;
        String Search;
        int selectedIndex = cmbKategori.getSelectedIndex();
        if (selectedIndex == 0){
            kategori = "kode";
        }else{
            kategori = "nama";
        }
        Search = txtSearch.getText();
        
        try {
            String sql = "SELECT * FROM tbl_matkul WHERE " + kategori + " LIKE ?";
            Connection conn = koneksi.getConnection();
            PreparedStatement stmt = conn.prepareStatement(sql);
            stmt.setString(1, "%" + Search + "%");
            ResultSet rs = stmt.executeQuery();
            while (rs.next()) {
                String data[] = { rs.getString(1), rs.getString(2), rs.getString(3), rs.getString(4), rs.getString(5), rs.getString(6), rs.getString(7) };
                model.addRow(data);
            }
        } catch (Exception e) {
            System.out.println("Ambil Data Gagal");
        }
    }
