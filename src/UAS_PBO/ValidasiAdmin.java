    private void btnValidasiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnValidasiActionPerformed
        
         int selectedRow = tblUser.getSelectedRow();
        if (selectedRow != -1) {
            String id = tblUser.getValueAt(selectedRow, 0).toString();
            int confirm = JOptionPane.showConfirmDialog(null, "Apakah Anda yakin ingin melakukan validasi?", "Confirm Validasi", JOptionPane.YES_NO_OPTION);
            if (confirm == JOptionPane.YES_OPTION) {
                try {
                    Connection conn = koneksi.getConnection();
                    PreparedStatement stmt = conn.prepareStatement("UPDATE tbl_user SET validasi = 'Sudah' WHERE id_user = ?");
                    stmt.setString(1, id);
                    int rowsAffected = stmt.executeUpdate();
                    if (rowsAffected > 0) {
                        JOptionPane.showMessageDialog(null, "Validasi Berhasil");
                        tampilData(); // Refresh the table data
                    } else {
                        JOptionPane.showMessageDialog(null, "Gagal Melakukan Validasi");
                    }
                } catch (Exception ex) {
                    System.out.println(ex.getMessage());
                }
            }
        } else {
            JOptionPane.showMessageDialog(null, "Belum Ada Data Yang Dipilih");
        }       
        
    }//GEN-LAST:event_btnValidasiActionPerformed

