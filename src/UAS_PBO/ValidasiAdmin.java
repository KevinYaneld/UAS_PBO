    public void tampilData() {
        int row = tblUser.getRowCount();
        for (int i = 0; i < row; i++) {
            model.removeRow(0);
        }
        String Search;
        Search = txtSearch.getText();

        try {
            String sql = "SELECT id_user, username, validasi FROM tbl_user WHERE username LIKE ? AND role = 'User'";
            Connection conn = koneksi.getConnection();
            PreparedStatement stmt = conn.prepareStatement(sql);
            stmt.setString(1, "%" + Search + "%");
            ResultSet rs = stmt.executeQuery();
            while (rs.next()) {
                String data[] = { rs.getString(1), rs.getString(2), rs.getString(3) };
                model.addRow(data);
            }
        } catch (Exception e) {
            System.out.println("Ambil Data Gagal: " + e.getMessage());
        }
    }
