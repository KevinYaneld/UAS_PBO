    private void tblUserMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblUserMouseClicked
        int selectedRow = tblUser.getSelectedRow();
        if (selectedRow != -1) {
            String status = tblUser.getValueAt(selectedRow, 2).toString();
            if (status.equals("Belum")) {
                btnValidasi.setEnabled(true);
            } else if (status.equals("Sudah")) {
                btnValidasi.setEnabled(false);
            } else {
                btnValidasi.setEnabled(false);
            }
            User.getInstance().setUsername(tblUser.getValueAt(selectedRow, 0).toString());
        } 
    }//GEN-LAST:event_tblUserMouseClicked

