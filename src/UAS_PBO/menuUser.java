    private void btnHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHapusActionPerformed
        int selectedRow = tblKRS.getSelectedRow();
        if (selectedRow != -1) {
            int confirm = JOptionPane.showConfirmDialog(null, "Apakah Anda Yakin Ingin Menghapus?", "Confirm Deletion", JOptionPane.YES_NO_OPTION);
            if (confirm == JOptionPane.YES_OPTION) {
                String nama = tblKRS.getValueAt(selectedRow, 2).toString();
                try {
                    Connection conn = koneksi.getConnection();
                    PreparedStatement stmt = conn.prepareStatement("DELETE FROM tbl_krs WHERE nama = ? and id_akun = ?");
                    stmt.setString(1, nama);
                    stmt.setString(2, User.getInstance().getUsername());
                    int rowsAffected = stmt.executeUpdate();
                    if (rowsAffected > 0) {
                        JOptionPane.showMessageDialog(null, "Berhasil Dihapus");
                        tampilData();
                    }
                } catch (SQLException ex) {
                    System.out.println(ex.getMessage());
                }
            }
        } else {
            JOptionPane.showMessageDialog(null, "Belum Ada Data Yang Dipilih");
        }
    }//GEN-LAST:event_btnHapusActionPerformed

