    public void tampilData() {
        hitungSKS();
        int row = tblKRS.getRowCount();
        for (int i = 0; i < row; i++) {
            model.removeRow(0);
        }
        try {
            String sql = "SELECT kelas, kode, nama, wp, semester, sks FROM tbl_krs WHERE id_akun = ?";
            Connection conn = koneksi.getConnection();
            PreparedStatement ps = conn.prepareStatement(sql);
            ps.setString(1, User.getInstance().getUsername()); 
            ResultSet rs = ps.executeQuery();
            while (rs.next()) {
                String data[] = {
                    rs.getString(1),
                    rs.getString(2),
                    rs.getString(3),
                    rs.getString(4),
                    rs.getString(5),
                    rs.getString(6)
                };
                model.addRow(data);
            }
        } catch (Exception e) {
            System.out.println("Ambil Data Gagal");
    }
}
    
    public void cekValidasi(){
        try {
        Connection conn = koneksi.getConnection();
        PreparedStatement ps = conn.prepareStatement("SELECT Validasi FROM tbl_user WHERE id_user = ?");
        ps.setString(1, User.getInstance().getUsername());

        ResultSet rs = ps.executeQuery();

        if (rs.next()) {
            String validasi = rs.getString("Validasi");
            if (validasi.equals("Belum")) {
                btnTambah.setVisible(true);
                btnCetak.setVisible(false);
                btnCetak.setEnabled(false);
                btnHapus.setVisible(true);
            } else {
                btnTambah.setVisible(false);
                btnTambah.setEnabled(false);
                btnCetak.setVisible(true);
                btnHapus.setVisible(false);
                btnHapus.setEnabled(false);
            }
        }
        } catch (SQLException ex) {
            System.out.println(ex.getMessage());
            // Handle any potential exceptions
        }
    }
    
    public void hitungSKS(){
        try {
            Connection conn = koneksi.getConnection();
            PreparedStatement stmt = conn.prepareStatement("SELECT SUM(sks) FROM tbl_krs WHERE id_akun = ?");
            stmt.setString(1, User.getInstance().getUsername());

            ResultSet rs = stmt.executeQuery();

            if (rs.next()) {
                String totalSks = rs.getString(1);
                txtSKS.setText(totalSks);
            }
            
        } catch (SQLException ex) {
            System.out.println(ex.getMessage());
            // Handle any potential exceptions
        }
    }
